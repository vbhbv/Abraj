    def get_detailed_report(self, chart_data: Any) -> str:
        """يولد التقرير التقني الكامل المحدث الخالي تماماً من نصوص نقص التحديثات"""
        report = ["🪐 **البيانات الفلكية التفصيلية والدقيقة**\n"]
        
        for p_name, p_data in chart_data.planets.items():
            pl_name_ar, pl_emoji = self._get_planet_info(p_name)
            sg_name_ar, sg_emoji = self._get_sign_info(p_data.sign)
            
            retro_str = " ⟨تراجع⟩ 🔄" if p_data.retrograde else ""
            
            # جلب التفسير الذكي: يبحث أولاً في الـ JSON، وإذا لم يجده أو وجد نصاً فارغاً ينتقل للقاموس المدمج الاحترافي فوراً
            interpretation = self.planet_sign_db.get(p_name, {}).get(p_data.sign, "")
            if not interpretation or "سيضاف قريباً" in interpretation or "التحديثات" in interpretation:
                interpretation = self.fallback_sign_db.get(p_name, {}).get(p_data.sign, "تحليل طاقة هذا الموضع متاح ديناميكياً عبر الأزرار.")
                
            house_interp = self.planet_house_db.get(p_name, {}).get(p_data.house, "")
            
            p_text = f"{pl_emoji} **{pl_name_ar} في {sg_name_ar} {sg_emoji}** (الدرجة {p_data.degree}°){retro_str}\n"
            p_text += f"🏠 في البيت {p_data.house}\n"
            
            if interpretation:
                p_text += f"📜 *دلالة البرج:* {interpretation}\n"
            if house_interp:
                p_text += f"🧭 *دلالة البيت:* {house_interp}\n"
                
            report.append(p_text)
            
        return "\n---\n".join(report)
